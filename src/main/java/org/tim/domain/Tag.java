package org.tim.domain;

/**
 * @author tim
 */
public class Tag extends Entity {
    private String name;

    public Tag() {
    }

    public Tag(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
